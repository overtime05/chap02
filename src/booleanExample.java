
public class booleanExample {

	public static void main(String[] args) {
		//[boolean변수] 조건식이면 가능하다 - t,f를 구분할 조건식
		boolean tf1 = true;
		boolean tf2 = false;
		boolean tf3 = 10 > 5;  //true
		boolean tf4 = 10.5 < 0;  //false
//		boolean tf5 = 10 + 5; // (x)
		
		
		System.out.println("tf1=" + tf1);
		System.out.println("tf2=" + tf2);
		System.out.println("tf3=" + tf3);
		System.out.println("tf4=" + tf4);
		
		
		
	}

}
